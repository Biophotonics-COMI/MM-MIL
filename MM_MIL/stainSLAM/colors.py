all_color = ['cyanhot', 'chot', 'greenhot', 'ghot', 'magnettahot', 'mhot', 'yellowhot', 'yhot']


def select_cmap(cmap_name):
    """
    return cmap list
    :param cmap_name: (str) the name of the cmap
    :return cmap_array: (2d array) with shape (, 3)
    """
    assert cmap_name.lower() in all_color, '[Error] please choose from {}'.format(all_color)
    if cmap_name.lower() in ['cyanhot', 'chot']:
        return np.array(cyanhot)
    elif cmap_name.lower() in ['greenhot', 'ghot']:
        return np.array(greenhot)
    elif cmap_name.lower() in ['magnettahot', 'mhot']:
        return np.array(magnettahot)
    elif cmap_name.lower() in ['yellowhot', 'yhot']:
        return np.array(yellowhot)


